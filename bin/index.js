#!/usr/bin/env node
import fs   from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const majahData = JSON.parse(fs.readFileSync(path.join(__dirname, 'majah.json'), 'utf8'));
const { hadiths, chapters, metadata } = majahData;

const args = process.argv.slice(2);

// ── ANSI colours (no dependencies) ───────────────────────────────────────────
const c = {
  reset:   '\x1b[0m',
  bold:    '\x1b[1m',
  dim:     '\x1b[2m',
  cyan:    '\x1b[36m',
  green:   '\x1b[32m',
  yellow:  '\x1b[33m',
  magenta: '\x1b[35m',
  white:   '\x1b[97m',
  gray:    '\x1b[90m',
};
const cyan    = s => `${c.cyan}${c.bold}${s}${c.reset}`;
const green   = s => `${c.green}${s}${c.reset}`;
const yellow  = s => `${c.yellow}${s}${c.reset}`;
const magenta = s => `${c.magenta}${s}${c.reset}`;
const bold    = s => `${c.bold}${s}${c.reset}`;
const dim     = s => `${c.dim}${s}${c.reset}`;
const gray    = s => `${c.gray}${s}${c.reset}`;

const divider      = () => gray('═'.repeat(60));
const thinDivider  = () => gray('─'.repeat(60));

// ── Helpers ───────────────────────────────────────────────────────────────────
function getChapterName(chapterId) {
  const ch = chapters.find(c => c.id === chapterId);
  return ch ? ch.english : 'Unknown Chapter';
}

function highlightMatch(text, query) {
  if (!query) return text;
  const re = new RegExp(`(${query.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')})`, 'gi');
  return text.replace(re, `${c.green}${c.bold}$1${c.reset}`);
}

function wrapText(text, indent = 2, width = 78) {
  const words = text.split(' ');
  const lines = [];
  let line = ' '.repeat(indent);
  for (const word of words) {
    if (line.length + word.length + 1 > width) {
      lines.push(line);
      line = ' '.repeat(indent) + word;
    } else {
      line += (line.trim() ? ' ' : '') + word;
    }
  }
  if (line.trim()) lines.push(line);
  return lines.join('\n');
}

// ── Print a single hadith (full view) ────────────────────────────────────────
function printHadith(h, mode, query) {
  if (!h) { console.log(yellow('\n  Hadith not found.\n')); return; }
  const chName = getChapterName(h.chapterId);

  console.log('\n' + divider());

  if (mode === '-a') {
    // Arabic only
    console.log(`\n  ${cyan('Hadith #' + h.id)}  ${gray('|')}  ${bold('Chapter: ' + h.chapterId)} ${gray('—')} ${yellow(chName)}\n`);
    console.log(wrapText(h.arabic) + '\n');
  } else if (mode === '-b') {
    // Arabic + English
    console.log(`\n  ${cyan('Hadith #' + h.id)}  ${gray('|')}  ${bold('Chapter: ' + h.chapterId)} ${gray('—')} ${yellow(chName)}\n`);
    console.log(wrapText(h.arabic) + '\n');
    console.log(`  ${bold('Narrator:')} ${magenta(h.english.narrator)}\n`);
    console.log(wrapText(h.english.text) + '\n');
  } else {
    // English (default)
    console.log(`\n  ${cyan('Hadith #' + h.id)}  ${gray('|')}  ${bold('Chapter: ' + h.chapterId)} ${gray('—')} ${yellow(chName)}\n`);
    console.log(`  ${bold('Narrator:')} ${magenta(query ? highlightMatch(h.english.narrator, query) : h.english.narrator)}\n`);
    const text = query ? highlightMatch(h.english.text, query) : h.english.text;
    console.log(wrapText(text) + '\n');
  }

  console.log(divider() + '\n');
}

// ── React hook generator ──────────────────────────────────────────────────────
function generateReactHook() {
  const hookSrc = `// Auto-generated by sunan-ibn-majah CLI
import { useState, useEffect } from 'react';
import { loadMajah } from 'sunan-ibn-majah';

export function useMajah() {
  const [majah, setMajah] = useState(null);
  useEffect(() => { loadMajah().then(setMajah); }, []);
  return majah;
}
`;
  const hooksDir = path.join(process.cwd(), 'src', 'hooks');
  if (!fs.existsSync(hooksDir)) fs.mkdirSync(hooksDir, { recursive: true });
  fs.writeFileSync(path.join(hooksDir, 'useMajah.js'), hookSrc, 'utf8');
  console.log(green('\n✓ Generated: src/hooks/useMajah.js\n'));
}

// ── --help ────────────────────────────────────────────────────────────────────
if (!args.length || args[0] === '--help') {
  const pkg = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
  console.log(`
${divider()}
  ${cyan('majah')}  ${gray('·')}  ${bold('Sunan Ibn Majah')}  ${gray('v' + pkg.version)}
${divider()}

  ${bold('Usage:')}

    ${cyan('majah')} ${yellow('<id>')}                   Hadith by ID
    ${cyan('majah')} ${yellow('<id>')} ${gray('-a')}                Arabic only
    ${cyan('majah')} ${yellow('<id>')} ${gray('-b')}                Arabic + English
    ${cyan('majah')} ${yellow('<chapter>')} ${yellow('<n>')}           nth hadith of a chapter
    ${cyan('majah')} ${gray('--search')} ${yellow('"<query>"')}       Search in English text
    ${cyan('majah')} ${gray('--search')} ${yellow('"<query>"')} ${gray('--all')}   Show all results
    ${cyan('majah')} ${gray('--random')}                 Random hadith
    ${cyan('majah')} ${gray('--chapter')} ${yellow('<id>')}           All hadiths in chapter
    ${cyan('majah')} ${gray('--chapters')}               List all chapters
    ${cyan('majah')} ${gray('--info')}                   Book metadata
    ${cyan('majah')} ${gray('--react')}                  Generate React hook
    ${cyan('majah')} ${gray('--version')}
    ${cyan('majah')} ${gray('--help')}

${divider()}
`);
  process.exit(0);
}

// ── --version ─────────────────────────────────────────────────────────────────
if (args[0] === '--version') {
  const pkg = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
  console.log(`\n  ${cyan('sunan-ibn-majah')} ${gray('v' + pkg.version)}\n`);
  process.exit(0);
}

// ── --react ───────────────────────────────────────────────────────────────────
if (args[0] === '--react') {
  generateReactHook();
  process.exit(0);
}

// ── --info ────────────────────────────────────────────────────────────────────
if (args[0] === '--info') {
  console.log(`
${divider()}
  ${cyan('Sunan Ibn Majah')}
${divider()}

  ${bold('Title:')}        ${metadata.english.title}
  ${bold('Author:')}       ${metadata.english.author}
  ${bold('Total Hadiths:')} ${yellow(String(hadiths.length))}
  ${bold('Chapters:')}     ${yellow(String(chapters.length))}

${divider()}
`);
  process.exit(0);
}

// ── --chapters ────────────────────────────────────────────────────────────────
if (args[0] === '--chapters') {
  console.log(`\n${divider()}\n  ${cyan('Chapters')} ${gray('—')} ${bold('Sunan Ibn Majah')}\n${divider()}\n`);
  chapters.forEach(ch => {
    console.log(`  ${cyan('#' + String(ch.id).padEnd(5))} ${ch.english}`);
  });
  console.log(`\n${divider()}\n`);
  process.exit(0);
}

// ── --random ──────────────────────────────────────────────────────────────────
if (args[0] === '--random') {
  const h = hadiths[Math.floor(Math.random() * hadiths.length)];
  printHadith(h, args[1]);
  process.exit(0);
}

// ── --chapter <id> ────────────────────────────────────────────────────────────
if (args[0] === '--chapter') {
  const chapterId = parseInt(args[1]);
  const results   = hadiths.filter(h => h.chapterId === chapterId);
  const chName    = getChapterName(chapterId);
  if (!results.length) { console.log(yellow(`\n  No hadiths found for chapter ${chapterId}.\n`)); process.exit(0); }
  console.log(`\n${divider()}\n  ${cyan('Chapter ' + chapterId)} ${gray('—')} ${bold(chName)}  ${gray('(' + results.length + ' hadiths)')}\n${divider()}`);
  results.forEach(h => {
    console.log(`\n  ${cyan('Hadith #' + h.id)}`);
    console.log(`  ${bold('Narrator:')} ${magenta(h.english.narrator)}`);
    console.log(wrapText(h.english.text));
    console.log('\n' + thinDivider());
  });
  console.log('');
  process.exit(0);
}

// ── --search <query> [--all] ──────────────────────────────────────────────────
if (args[0] === '--search') {
  const showAll  = args.includes('--all');
  const allIdx   = args.indexOf('--all');
  const queryArgs = args.slice(1).filter(a => a !== '--all');
  const query    = queryArgs.join(' ').replace(/^["']|["']$/g, '');

  if (!query) {
    console.log(yellow('\n  Usage: majah --search "<query>" [--all]\n'));
    process.exit(1);
  }

  const t0      = Date.now();
  const results = hadiths.filter(h =>
    h.english?.text?.toLowerCase().includes(query.toLowerCase()) ||
    h.english?.narrator?.toLowerCase().includes(query.toLowerCase())
  );
  const ms = Date.now() - t0;

  console.log('\n' + divider());
  console.log(`  ${bold('Search:')} ${yellow('"' + query + '"')}  ${gray('—')}  ${cyan(results.length + ' results')}  ${gray('(' + ms + 'ms)')}`);
  console.log(divider());

  if (!results.length) {
    console.log(yellow(`\n  No results for "${query}"\n`));
    process.exit(0);
  }

  const toShow = showAll ? results : results.slice(0, 5);

  toShow.forEach((h, i) => {
    const chName = getChapterName(h.chapterId);
    console.log(`\n  ${cyan('#' + (i + 1))}  ${bold('Hadith ' + h.id)}  ${gray('|')}  ${bold('Chapter: ' + h.chapterId)} ${gray('—')} ${yellow(chName)}`);
    console.log(`  ${bold('Narrator:')} ${magenta(highlightMatch(h.english.narrator, query))}`);
    console.log(wrapText(highlightMatch(h.english.text, query)));
    console.log('\n' + thinDivider());
  });

  if (!showAll && results.length > 5) {
    console.log(`\n  ${dim('Showing')} ${cyan('5')} ${dim('of')} ${cyan(String(results.length))} ${dim('results.')}  ${yellow('Run with --all to see all results')}`);
    console.log(`  ${gray('majah --search "' + query + '" --all')}\n`);
  } else {
    console.log(`\n  ${dim('Showing all')} ${cyan(String(results.length))} ${dim('results.')}\n`);
  }

  process.exit(0);
}

// ── majah <chapter> <n> ───────────────────────────────────────────────────────
if (args.length >= 2 && !isNaN(parseInt(args[0])) && !isNaN(parseInt(args[1]))) {
  const chapterId = parseInt(args[0]);
  const n         = parseInt(args[1]);
  const inChapter = hadiths.filter(h => h.chapterId === chapterId);
  const h         = inChapter[n - 1];
  printHadith(h, args[2]);
  process.exit(0);
}

// ── majah <id> [-a|-b] ────────────────────────────────────────────────────────
const id = parseInt(args[0]);
if (isNaN(id)) {
  console.log(yellow(`\n  Unknown command: ${args[0]}. Run majah --help\n`));
  process.exit(1);
}
const h = hadiths.find(h => h.id === id);
printHadith(h, args[1]);

#!/usr/bin/env node
'use strict';

const fs   = require('fs');
const path = require('path');

const majahData = JSON.parse(fs.readFileSync(path.join(__dirname, 'majah.json'), 'utf8'));
const { hadiths, chapters } = majahData;

const args = process.argv.slice(2);

function printHadith(h, mode) {
  if (!h) { console.log('Hadith not found.'); return; }
  if (mode === '-a') {
    console.log(`\n[${h.id}] ${h.arabic}\n`);
  } else if (mode === '-b') {
    console.log(`\n[${h.id}]`);
    console.log(`Arabic:  ${h.arabic}`);
    console.log(`Narrator: ${h.english.narrator}`);
    console.log(`English: ${h.english.text}\n`);
  } else {
    console.log(`\n[${h.id}] ${h.english.narrator}`);
    console.log(h.english.text + '\n');
  }
}

function generateReactHook() {
  const hookSrc = `// Auto-generated by sunan-ibn-majah CLI
import { useState, useEffect } from 'react';
import { loadMajah } from 'sunan-ibn-majah';

export function useMajah() {
  const [majah, setMajah] = useState(null);
  useEffect(() => { loadMajah().then(setMajah); }, []);
  return majah;
}
`;
  const hooksDir = path.join(process.cwd(), 'src', 'hooks');
  if (!fs.existsSync(hooksDir)) fs.mkdirSync(hooksDir, { recursive: true });
  fs.writeFileSync(path.join(hooksDir, 'useMajah.js'), hookSrc, 'utf8');
  console.log('✓ Generated: src/hooks/useMajah.js');
}

if (!args.length || args[0] === '--help') {
  console.log(`
sunan-ibn-majah CLI

  majah <id>           Hadith by ID
  majah <id> -a        Arabic only
  majah <id> -b        Arabic + English
  majah <chapter> <n>  nth hadith in chapter
  majah --react        Generate React hook (useMajah.js)
  majah --version
  majah --help
`);
  process.exit(0);
}

if (args[0] === '--version') {
  const pkg = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf8'));
  console.log(pkg.version);
  process.exit(0);
}

if (args[0] === '--react') {
  generateReactHook();
  process.exit(0);
}

// majah <chapter> <index>
if (args.length >= 2 && !args[1].startsWith('-')) {
  const chapterId = parseInt(args[0]);
  const n         = parseInt(args[1]);
  const inChapter = hadiths.filter(h => h.chapterId === chapterId);
  const h         = inChapter[n - 1];
  printHadith(h, args[2]);
  process.exit(0);
}

// majah <id> [-a|-b]
const id = parseInt(args[0]);
const h  = hadiths.find(h => h.id === id);
printHadith(h, args[1]);
